#include<iostream>
using namespace std;

int main(){

    cout << " 10 > 5 \t\t"  << (10 > 5)   << endl;
    cout << " 10 >= 5 \t\t" << (10 >= 5)  << endl;
    cout << " 10 < 5 \t\t"  << (10 < 5)   << endl;
    cout << " 10 <= 5 \t\t" << (10 <= 5) << endl;
    cout << " 10 == 5 \t\t" << (10 == 5) << endl;
    cout << " 10 != 5 \t\t" << (10 != 5) << endl;

}