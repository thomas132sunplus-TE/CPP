#include <iostream>
using namespace std;

int main() {
    cout << "sizeof(bool)\t" << sizeof(bool)   << "\n";
    cout << "sizeof(true)\t" << sizeof(true)   << "\n";
    cout << "sizeof(false)\t" << sizeof(false) << "\n";

    cout << "true\t" << true  << "\n";
    cout << "false\t" << false << "\n";

    return 0;
}